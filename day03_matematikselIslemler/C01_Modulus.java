package day03_matematikselIslemler;

public class C01_Modulus {

    public static void main(String[] args) {

        System.out.println( 20  % 3 ); // 2

        System.out.println( 45 % 5 ); // 0 ==> sayi 5 ile tam bolunuyor

        System.out.println( 23 % 2 ); // 1 ==> sayilarin tek-cift sayi oldugunu kontrol icin kullaniriz

        System.out.println(245 % 10 ); // 5 ==> birler basamagini bulmak icin

        System.out.println( 245 / 10 ); // 24.5 ==> 24 ==>
                                        // birler basamagindan kurtulmak istersek kullanilir



    }
}
